import { execSync } from 'child_process';
import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const { version } = JSON.parse(readFileSync(resolve(__dirname, '../package.json'), 'utf8'));

let commitId = 'unknown';
try {
  commitId = execSync('git rev-parse --short HEAD', { encoding: 'utf8' }).trim();
} catch {
  console.warn('Could not determine git commit hash.');
}

const output = `\
// This file is auto-generated by scripts/generate-build-info.mjs. Do not edit manually.
export const buildVersion = '${version}';
export const buildCommitId = '${commitId}';
`;

const outPath = resolve(__dirname, '../src/environments/build-info.generated.ts');
writeFileSync(outPath, output);
console.log(`Build info written: version=${version}, commitId=${commitId}`);
