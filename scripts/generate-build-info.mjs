import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, '..');
const webPackageJsonPath = resolve(repoRoot, 'web/package.json');
const outputPath = resolve(
  repoRoot,
  'web/src/environments/build-info.generated.ts'
);

const { version } = JSON.parse(readFileSync(webPackageJsonPath, 'utf8'));
const match = /^(\d+)\.(\d+)/.exec(version ?? '');
if (!match) {
  throw new Error(
    `Cannot parse major.minor from web/package.json version: "${version}"`
  );
}
const [, major, minor] = match;

const buildNumber = tryGit('git rev-list --count HEAD') ?? '0';
const commitId = tryGit('git rev-parse --short=8 HEAD') ?? 'unknown';

const buildVersion = `${major}.${minor}.${buildNumber}`;

const contents = `// This file is auto-generated by scripts/generate-build-info.mjs. Do not edit manually.
export const buildVersion = '${buildVersion}';
export const buildCommitId = '${commitId}';
`;

let existing = '';
try {
  existing = readFileSync(outputPath, 'utf8');
} catch {
  // File does not exist yet; treat as empty.
}

if (existing !== contents) {
  writeFileSync(outputPath, contents);
  console.log(`[build-info] wrote ${buildVersion} (${commitId})`);
} else {
  console.log(`[build-info] unchanged ${buildVersion} (${commitId})`);
}

function tryGit(command) {
  try {
    return execSync(command, {
      cwd: repoRoot,
      stdio: ['ignore', 'pipe', 'ignore'],
    })
      .toString()
      .trim();
  } catch {
    return null;
  }
}
